const mongoose = require('mongoose');

const product = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    quantity: {
        type: Number,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
});

const Product = mongoose.model('Product', product);

module.exports = Product;


